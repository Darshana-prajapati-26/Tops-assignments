# String slicing example
text = "Hello, Python World!"

# Access from index 0 to 4
print("First 5 characters:", text[0:5])

# Access from index 7 to 12
print("Characters from index 7 to 12:", text[7:13])

# Access from the beginning to index 4
print("Up to index 4:", text[:5])

# Access from index 7 to the end
print("From index 7 onwards:", text[7:])

# Access the last 6 characters
print("Last 6 characters:", text[-6:])

# Access every second character
print("Every second character:", text[::2])
