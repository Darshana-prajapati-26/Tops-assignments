#Practical Example 1 – Print "Hello" using a string
print("Hello")

#Practical Example 2 – Allocate a string to a variable and print it
message = "Welcome to Python"
print(message)

#Practical Example 3 – Print a string using triple quotes
multi_line_string = """This is a
multi-line
string"""
print(multi_line_string)

#Practical Example 4 – Access the first character of a string using index
text = "Python"
print(text[0])  # Index 0 is the first character

#Practical Example 5 – Access string from the second position onwards using slicing
text = "Python"
print(text[1:])  # From index 1 to the end

#Practical Example 6 – Access a string up to the fifth character
text = "Python"
print(text[:5])  # From start to index 4 (5 not included)

#Practical Example 7 – Print substring between index 1 and 4
text = "Python"
print(text[1:5])  # From index 1 to 4 (5 not included)

#Practical Example 8 – Print string from the last character
text = "Python"
print(text[-1])  # Negative index -1 is the last character

#Practical Example 9 – Print every alternate character starting from index 1
text = "Python"
print(text[1::2])  # Start at index 1, step 2
