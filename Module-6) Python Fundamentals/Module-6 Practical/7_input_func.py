#Practical Example 3: How to take user input using input()

# Taking user input

# Input as string
name = input("Enter your name: ")
print("Hello,", name)

# Input as integer
age = int(input("Enter your age: "))
print("Age after 5 years:", age + 5)

# Input as float
height = float(input("Enter your height in meters: "))
print("Height:", height)
