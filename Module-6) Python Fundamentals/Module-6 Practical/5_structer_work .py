# Practical Example 1: How does Python code structure work?

# 1. Import libraries (if needed)
import math

# 2. Define variables
radius = 5

# 3. Define functions
def calculate_area(r):
    return math.pi * r * r

# 4. Main program (execution starts here)
print("Program starts...")
area = calculate_area(radius)
print("Area of circle:", area)
print("Program ends...")
