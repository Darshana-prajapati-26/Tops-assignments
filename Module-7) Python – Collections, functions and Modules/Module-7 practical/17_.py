#Write a Python program to import the math module and use functions like sqrt(), ceil(), floor().

# Import math module
import math

num = 7.5

# Square root
sqrt_value = math.sqrt(num)
print("Square root of", num, "is", sqrt_value)

# Ceiling value (round up)
ceil_value = math.ceil(num)
print("Ceiling value of", num, "is", ceil_value)

# Floor value (round down)
floor_value = math.floor(num)
print("Floor value of", num, "is", floor_value)
